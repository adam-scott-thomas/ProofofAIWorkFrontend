function hash(seed: string) {
  return `${seed}${"0".repeat(64)}`.slice(0, 64);
}

function evidence(title: string, turns: string[], kind = "evidence") {
  return {
    id: title.toLowerCase().replace(/[^a-z0-9]+/g, "-").replace(/^-|-$/g, ""),
    title,
    summary: title,
    kind,
    turns,
  };
}

