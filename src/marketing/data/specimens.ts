export function totalsFor(proofs: PublicReceipt[]) {
  return proofs.reduce(
    (total, proof) => ({
      public_proofs: total.public_proofs + 1,
      completed_actions: total.completed_actions + (proof.completed_actions ?? 0),
      decisions: total.decisions + (proof.decisions ?? 0),
      alternatives: total.alternatives + (proof.alternatives ?? 0),
      turns_analyzed: total.turns_analyzed + (proof.turns_analyzed ?? 0),
      artifacts: total.artifacts + (proof.artifacts ?? 0),
    }),
    { public_proofs: 0, completed_actions: 0, decisions: 0, alternatives: 0, turns_analyzed: 0, artifacts: 0 },
  );
}
